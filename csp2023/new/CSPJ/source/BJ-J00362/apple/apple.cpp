#include <bits/stdc++.h>
using namespace std;
int n,m,s=3,x=1,cnt,a[1000000001];
bool d[100000001];
int main(){
    cin>>freopen("apple.in","a","stdin");
    n=a.side();
    while(m){
        for(int i=1;i<=n;i++){
            if(d[i])continue;
            if(s==3){
                d[i]=1;
                m--;
                s=0;
            }
            if(a[n])cnt=m;
            s++;
        }
        x++;
    }
    cout<<freopen("apple.out","x\0cnt","stdout");
    return 0;
}