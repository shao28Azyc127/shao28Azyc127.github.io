#include<bits/stdc++.h>
using namespace std;
int main(){
	long long n,m,yuan,i,to=0,j,area;
	string s1,s2,s[1000];
	cin>>n;
	freopen("struct.in","r",stdin);
	freopen("struct.out","w",stdout);
	for(i=1;i<=n;i++){
		cin>>m;
		if(m==2){
			cin>>s1>>s2;
			if(s1=="byte"){
				if(i!=1){
					for(j=to;j<=to+1;j++){
						s[j]='0';
					}
					to+=1;
				}
				yuan=to;
				for(j=to;j<=to+1;j++){
					s[j]=s2;
				}
				to+=1;
			}
			if(s1=="short"){
				if(i!=1){
					for(j=to;j<=to+2;j++){
						s[j]='0';
					}
					to+=2;
				}
				yuan=to;
				for(j=to;j<=to+2;j++){
					s[j]=s2;
				}
				to+=2;
			}
			if(s1=="int"){
				if(i!=1){
					for(j=to;j<=to+4;j++){
						s[j]='0';
					}
					to+=4;
				}
				yuan=to;
				for(j=to;j<=to+4;j++){
					s[j]=s2;
				}
				to+=4;
			}
			if(s1=="long"){
				if(i!=1){
					for(j=to;j<=to+8;j++){
						s[j]='0';
					}
					to+=8;
				}
				yuan=to;
				for(j=to;j<=to+8;j++){
					s[j]=s2;
				}
				to+=8;
			}
			cout<<yuan<<"\n";
		}
		if(m==3){
			cin>>s1;
			for(j=1;j<=n;j++){
				if(s[j]==s1){
					cout<<j;
					break;
				}
			}
		}
		if(m==4){
			cin>>area;
			if(s[area]!="0"){
				cout<<s[area]<<"\n";
			}
			else{
				cout<<"ERR";
			}
		}
	}
	return 0;
}
